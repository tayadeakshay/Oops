package com.oops.encapsulation;

public class Encapsulate {

	private int rollNo;
	private String name;
	private int age;

	public int getRollNo() {
		return rollNo;
	}

	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}

	public void setRollNo(int roll) {
		this.rollNo = roll;
	}

	public void setName(String name) {
		this.name = name;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
